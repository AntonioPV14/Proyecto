    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        SqlUsuarios modSql = new SqlUsuarios();
        usuarios mod = new usuarios();
        String pass = new String(txtPassword1.getPassword());
        String passCon = new String(txtPassword2.getPassword());

        if (pass.equals(passCon)) {

            if (modSql.existeUsuario(txtNombreUsuario.getText()) == 0) {

                mod.setNombreUsuario(txtNombreUsuario.getText());
                mod.setPassword(passCon);
                mod.setCI(txtCI.getText());
                mod.setNomApe(txtNomApe.getText());
                mod.setTelefono(txtTelefono.getText());

                if (modSql.registrar(mod)) {
                    JOptionPane.showMessageDialog(null, "Se ha registrado exitosamente");
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al registrar");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El Usuario ya existe");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El Password no coincide");
        }
    }//GEN-LAST:event_btnAceptarMouseClicked

