    private void txtTelefonoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtTelefonoMousePressed
    if (txtNombreUsuario.getText().isEmpty()) {
        txtNombreUsuario.setText(" Ingrese el nombre de usuario");
        txtNombreUsuario.setForeground(Color.gray);
    }
    if (String.valueOf(txtPassword1.getPassword()).isEmpty()) {
        txtPassword1.setText("********");
        txtPassword1.setForeground(Color.gray);
    }
    if (String.valueOf(txtPassword2.getPassword()).isEmpty()) {
        txtPassword2.setText("********");
        txtPassword2.setForeground(Color.gray);
    }
    if (txtCI.getText().isEmpty()) {
        txtCI.setText(" Ingresa el documento de identidad");
        txtCI.setForeground(Color.gray);
    }
    if (txtNomApe.getText().isEmpty()) {
        txtNomApe.setText(" Ingresa el nombre y apellido");
        txtNomApe.setForeground(Color.gray);
    }
    if (txtTelefono.getText().equals(" Ingresa tu numero telefonico")){
        txtTelefono.setText("");
        txtTelefono.setForeground(Color.black);
    }
    }//GEN-LAST:event_txtTelefonoMousePressed

